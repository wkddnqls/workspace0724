--1 학생이름과 주소지를표시하시오 헤더 / 학생이름 , 주소지 , 이름으로 오름차순

SELECT STUDENT_NAME AS "학생이름" ,STUDENT_ADDRESS "주소지"
FROM TB_STUDENT
ORDER BY STUDENT_NAME ASC;

--2 휴학중인 학생들의 이름과 주민번호를 나이가 적은 순서대로 화면에 출력
SELECT STUDENT_NAME AS "학생이름" ,STUDENT_SSN
FROM TB_STUDENT
WHERE ABSENCE_YN = 'Y'
ORDER BY STUDENT_SSN DESC;

--3 주소지가 강원도나 경기도인 학생들중 1900년대 학번을 가진 학생들의 이름, 학번 ,주소 출력
--  이름의 오름차순 , 헤더 "학생이름" "학번" "거주지 주소" 
SELECT STUDENT_NAME AS "학생이름" , STUDENT_NO "학번" , STUDENT_ADDRESS "거주지 주소"
FROM TB_STUDENT
WHERE (STUDENT_ADDRESS LIKE '%경기도%' OR STUDENT_ADDRESS LIKE '%강원도%')
AND SUBSTR(ENTRANCE_DATE,1,2) >'50' 
ORDER BY STUDENT_NAME;

-- 4 현재 법학과 교수 중 가장 나이가 많은 사람부터 이름을 확인 할 수 있는 SQL 문장을 작성
-- ( 법학과의 '학과코드' 는 학과 테이블을 조회해서 찾아내도록하자)
SELECT PROFESSOR_NAME
FROM TB_PROFESSOR
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NO = 005 
ORDER BY SUBSTR(PROFESSOR_SSN,1,2) ASC;

--5 2004년 2학기에 'C3118100' 과목을 수강한 학생들의 학점을 조회하려고 한다. 
-- 학점이 높은 학생부터 표시, 학점이 같으면 학번이 낮은학생부터 표시구문

SELECT STUDENT_NO, POINT
FROM TB_GRADE
WHERE CLASS_NO = 'C3118100'
  AND SUBSTR(TERM_NO, 1, 4) = '2004' 
  AND SUBSTR(TERM_NO, 5, 2) = '02'
ORDER BY POINT DESC, STUDENT_NO ASC;


--6 학생번호, 학생이름 , 학과이름을 / 학생이름으로 오름차순출력
SELECT STUDENT_NO , STUDENT_NAME , DEPARTMENT_NAME
FROM TB_STUDENT
