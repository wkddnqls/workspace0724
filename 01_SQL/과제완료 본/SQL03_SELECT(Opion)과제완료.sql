-- 10. ' 음악학과 ' 학생들의 평점을 구하려고한다. 음악학과 학생들의 학번,학생이름,전체평점 을 출력
SELECT STUDENT_NO , STUDENT_NAME , ROUND(AVG(POINT),1)
FROM TB_STUDENT
JOIN TB_GRADE USING(STUDENT_NO)
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NAME = '음악학과'
GROUP BY STUDENT_NO , STUDENT_NAME
;

--11 학번이 A313047인 학생이 학교에 나오지않고있다. 지도교수에게 내용을 전달하기 위한
-- 학과이름 , 학생이름, 지도 교수 이름이 필요하다.
-- 단 출력헤더 "학과이름" "학생이름 " 지도교수이름" 출력
SELECT 
  D.DEPARTMENT_NAME AS "학과이름", 
  S.STUDENT_NAME AS "학생이름", 
  P.PROFESSOR_NAME AS "지도교수이름"
FROM TB_STUDENT S
JOIN TB_DEPARTMENT D ON S.DEPARTMENT_NO = D.DEPARTMENT_NO
JOIN TB_PROFESSOR P ON COACH_PROFESSOR_NO = PROFESSOR_NO
WHERE S.STUDENT_NO = 'A313047';

-- 12 2007년도에 '인간관계론' 과목을 수강한 학생들 찾아 학생이름과 수강학기를 표시

SELECT STUDENT_NAME , TERM_NO 
FROM TB_STUDENT
JOIN TB_GRADE USING(STUDENT_NO)
JOIN TB_CLASS USING(CLASS_NO)
WHERE CLASS_NAME = '인간관계론' AND SUBSTR(TERM_NO, 1, 4) = '2007' ;

--13 예체능 계열 과목 중 과목 담당교수를 한명도 배정받지못한 과목을 찾아 
-- 과목이름과 학광이름 출력

SELECT C.CLASS_NAME, D.DEPARTMENT_NAME
FROM TB_CLASS C
JOIN TB_DEPARTMENT D ON C.DEPARTMENT_NO = D.DEPARTMENT_NO
LEFT JOIN TB_CLASS_PROFESSOR CP ON C.CLASS_NO = CP.CLASS_NO
WHERE D.CATEGORY = '예체능' AND CP.CLASS_NO IS NULL;


-- 14 춘 기술대학교 서반아어학과 학생들으 ㅣ지도교수를 게시
-- 학생이름 지도교수를 찾고 , 만일 지도교수가없으면 "지도교수 미지정으로 표시
-- 헤더 학생이름 , 지도교수 고학번부터
SELECT 
  S.STUDENT_NAME AS "학생이름",
  NVL(P.PROFESSOR_NAME, '지도교수 미지정') AS "지도교수"
FROM TB_STUDENT S
JOIN TB_DEPARTMENT D ON S.DEPARTMENT_NO = D.DEPARTMENT_NO
LEFT JOIN TB_PROFESSOR P ON S.COACH_PROFESSOR_NO = P.PROFESSOR_NO
WHERE D.DEPARTMENT_NAME LIKE '%서반%'
ORDER BY S.STUDENT_NO DESC;



--15 휴학생이아닌 학생 중 평점이 4.0이상인 학생을 찾아 그 학생의 학번,이름,학과이름, 평정 출력
SELECT 
  S.STUDENT_NO, 
  S.STUDENT_NAME, 
  D.DEPARTMENT_NAME, 
  ROUND(AVG(G.POINT), 1) AS 평점
FROM TB_STUDENT S
JOIN TB_DEPARTMENT D ON D.DEPARTMENT_NO = S.DEPARTMENT_NO
JOIN TB_GRADE G ON S.STUDENT_NO = G.STUDENT_NO
WHERE S.ABSENCE_YN = 'N'
GROUP BY S.STUDENT_NO, S.STUDENT_NAME, D.DEPARTMENT_NAME
HAVING AVG(G.POINT) >= 4.0
ORDER BY 평점 DESC;

--16 환경조경학과 전공과목들의 과목 별 평점을 파악할 수 잇는 SQL문을 작성하시오
SELECT CLASS_NO , CLASS_NAME , ROUND(AVG(POINT),1)
FROM TB_CLASS

