--- 18 국어국문학과에서 총 평점이 가장 높은학생의 이름과 학번을 표시하는 SQL문 작성
SELECT S.STUDENT_NAME, S.STUDENT_NO
FROM TB_STUDENT S
JOIN TB_DEPARTMENT D ON S.DEPARTMENT_NO = D.DEPARTMENT_NO
JOIN TB_GRADE G ON S.STUDENT_NO = G.STUDENT_NO
WHERE D.DEPARTMENT_NAME = '국어국문학과'
GROUP BY S.STUDENT_NAME, S.STUDENT_NO
HAVING AVG(G.POINT) = (
  SELECT MAX(평균평점)
  FROM (
    SELECT AVG(POINT) AS 평균평점
    FROM TB_STUDENT S2
    JOIN TB_DEPARTMENT D2 ON S2.DEPARTMENT_NO = D2.DEPARTMENT_NO
    JOIN TB_GRADE G2 ON S2.STUDENT_NO = G2.STUDENT_NO
    WHERE D2.DEPARTMENT_NAME = '국어국문학과'
    GROUP BY S2.STUDENT_NO
  )
);
SELECT STUDENT_NAME, STUDENT_NO
FROM TB_STUDENT
JOIN TB_DEPARTMENT  USING (DEPARTMENT_NO)
JOIN TB_GRADE  USING (STUDENT_NO)
WHERE DEPARTMENT_NAME = '국어국문학과'
GROUP BY STUDENT_NAME, STUDENT_NO
HAVING AVG(POINT) = (
  SELECT MAX(평균평점)
  FROM (
    SELECT AVG(POINT) AS 평균평점
    FROM TB_STUDENT
    JOIN TB_DEPARTMENT  USING (DEPARTMENT_NO)
    JOIN TB_GRADE  USING (STUDENT_NO)
    WHERE DEPARTMENT_NAME = '국어국문학과'
    GROUP BY STUDENT_NO
  )
);

-- 19 춘 기술대학교의 "환경조경학과"가 속한 같은 계열 학과들의 학과 별 전공과목 평점을 
-- 파악하기 위한 적절한 SQL 문은? 
--- 헤더 "계열 학과명" , "전공평점" 평점은 소수 한자리까지 반올림
SELECT DEPARTMENT_NAME, ROUND(AVG(POINT), 1) AS "전공평점"
FROM TB_GRADE
JOIN TB_CLASS USING (CLASS_NO)
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE CLASS_TYPE LIKE '%전공%' AND CATEGORY = (
    SELECT CATEGORY
    FROM TB_DEPARTMENT
    WHERE DEPARTMENT_NAME = '환경조경학과'
  )
GROUP BY DEPARTMENT_NAME;
